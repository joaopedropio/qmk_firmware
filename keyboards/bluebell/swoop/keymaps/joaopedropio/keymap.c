/* Function print spaceship oled*/
static void print_spaceship(void) {
    /* Render bullets */
    render_bullets();

    /*Render space*/
    render_space();

    /* Print current layer */
    oled_set_cursor(0, 14);
    oled_write("LAYER", false);

    oled_set_cursor(0, 15);
    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            oled_write("QWRTY", false);
            break;
        default:
            oled_write("COLMK", false);
    }
}

/* KEYBOARD SPACESHIP END */

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        /* KEYBOARD SPACESHIP STATUS START */
        case KC_F:
        case KC_U:
        case KC_UP:
            if (record->event.pressed) {
                if(y_spaceship > MIN_SPACESHIP_DIM) {
                    y_spaceship--;
                }
            }
            break;
        case KC_S:
        case KC_E:
        case KC_DOWN:
            if (record->event.pressed) {
                if(y_spaceship < MAX_SPACESHIP_DIM) {
                    y_spaceship++;
                }
            }
            break;
        case KC_R:
        case KC_N:
        case KC_LEFT:
            if (record->event.pressed) {
                if(x_spaceship > LEFT_SPACESHIP_DIM) { 
                    x_spaceship--;
                }
            }
            break;
        case KC_T:
        case KC_I:
        case KC_RGHT:
            if (record->event.pressed) {
                if(x_spaceship < RIGHT_SPACESHIP_DIM) {
                    x_spaceship++;
                }
            }
            break;

        /* Fire when pressing space*/
        case KC_LSFT:
            if (record->event.pressed) {
                for (int i = 0; i < MAX_BULLETS; i++) {
                    if(bullets[i] == -1) {
                        bullets[i] = y_spaceship - 1;
                        break;
                    }
                }
            }
            break;
        /* KEYBOARD SPACESHIP STATUS END */
    }
    return true;
}

oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    if (!is_keyboard_left()) {
        return OLED_ROTATION_180;  // flips the display 180 degrees if offhand
    }

    return OLED_ROTATION_270;
}

bool oled_task_user(void) {
    
    /* Get current WPM */
    current_wpm   = get_current_wpm();
    led_usb_state = host_keyboard_led_state();

    if (!is_keyboard_left()) {
        if (OLED_redraw) {
            oled_clear();
            last_bongo_frame = 12; //force a redraw
            draw_bongo_table();
             OLED_redraw = false;
        }
        draw_bongocat();
        //print_rubber_duck();
    } else {
        print_spaceship();
    }

    return false;
}
